package n3exercici1;

public class Bicycle {

	public void startUp() {
		System.out.println("You start riding the bike.");
	}
	
	public void accelerate() {
		System.out.println("You start to accelerate with the bike.");
	}
	
	public void slowDown() {
		System.out.println("You start to slow down with the bike.");
	}
	
	public void turnOff() {
		System.out.println("You stop riding the bike.");
	}
}
