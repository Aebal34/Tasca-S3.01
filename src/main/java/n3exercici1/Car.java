public class Car {

	public void startUp() {
		System.out.println("The car is started up.");
	}
	
	public void accelerate() {
		System.out.println("The car is accelerating.");
	}
	
	public void slowDown() {
		System.out.println("The car is slowing down.");
	}
	
	public void turnOff() {
		System.out.println("The car has been turned off.");
	}
}
