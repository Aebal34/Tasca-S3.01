package n3exercici1;

public class Plane {

	public void startUp() {
		System.out.println("Turbines on. Plane started up.");
	}
	
	public void accelerate() {
		System.out.println("Plane accelerating.");
	}
	
	public void slowDown() {
		System.out.println("Plane slowing down.");
	}
	
	public void turnOff() {
		System.out.println("Turbines off. Plane turned off.");
	}
}
